-- ==========================================================================
-- GradeSieve 录取逻辑 —— Access (Jet/ACE SQL) 纯 SQL 实现
--
-- 前提：三个表已通过"外部数据 → 导入 Excel"导入到当前 Access 数据库，
--       表名分别为 [表1] [表2] [表3]，列名保持原始中文表头：
--
--   表1 / 表2 列：序号、区县、就读学校、报名号、姓名、性别、身份证号、
--                 民族、学籍号、普高1、普高2、职教高考班、3+4志愿学校、
--                 语文、数学、英语、物理、化学
--   表3 列：     学校名称（格式 _代码_校名）、计划数
--
-- 业务规则：
--   1. 表1 中身份证号出现在表2 的，视为已被普高录取，剔除
--   2. 语数外三科总分 < 205 的，剔除
--   3. 按"职教高考班"志愿所属学校分组
--   4. 校内按 语数外总分(→语文→数学→英语) 降序排名
--   5. 各校按计划数从高分录取，超出者候补
--
-- ⚠️ 使用说明：
--   - Access 不支持一个查询里多条语句用分号批量执行。请逐条复制到
--     "查询 → SQL 视图"中运行（每条以分号结尾，运行下一条前清空或新建查询）。
--   - 步骤 1 给 [表1] 增加一列 [语数外总分]（ALTER + UPDATE），方便后面引用。
--   - 步骤 4 生成的 [录取结果] 是一张物理表，可双击打开查看、导出 Excel。
-- ==========================================================================


-- -------------------------------------------------------------------------
-- 步骤 1：给 [表1] 加一列 [语数外总分] 并填充
--         （Access 不能在子查询里重复计算，先物化成列最省事）
-- -------------------------------------------------------------------------
ALTER TABLE [表1] ADD COLUMN [语数外总分] INTEGER;

UPDATE [表1]
SET [语数外总分] = Nz([语文],0) + Nz([数学],0) + Nz([英语],0);


-- -------------------------------------------------------------------------
-- 步骤 2：生成"有效考生"查询 —— 剔除普高已录取 + 语数外不达标
--
-- 说明：
--   NOT EXISTS (SELECT 1 FROM [表2] WHERE 身份证号 = [表1].身份证号)
--   等价于：表1 的身份证号不在表2 中（即未被普高录取）
--   Nz() 用来把 NULL 折算成 0，避免空分数导致计算异常
-- -------------------------------------------------------------------------
-- 这一步不建表，只作为子查询供后面引用。
-- 如果你想单独查看有效考生，可把下面这段单独存成一个查询。


-- -------------------------------------------------------------------------
-- 步骤 3：生成带校内名次的查询，并存成物理表 [排名结果]
--
-- ★ Access 排名技巧（无窗口函数）：
--   对每个考生 t，"校内名次" = 该校内比他分数高的人数 + 1。
--   用 DCount 域聚合函数实现，语法是 Access 专属但稳定可靠。
--
--   DCount("*", "源", 条件) 返回满足条件的行数。
--   这里源用 [表1] 自身，条件：
--     - 同一所学校（职教高考班相同）
--     - 分数比我高（语数外总分比我大，或同分时 语→数→英 逐级比我高）
--
-- 注意：DCount 只能引用物理表，所以必须对 [表1] 操作，
--       而不能对"步骤2的有效考生子查询"操作。因此把"剔除"逻辑
--       合并到下面的 WHERE 里一起判断。
-- -------------------------------------------------------------------------
SELECT
    [表1].序号,
    [表1].区县,
    [表1].就读学校,
    [表1].报名号,
    [表1].姓名,
    [表1].性别,
    [表1].身份证号,
    [表1].民族,
    [表1].学籍号,
    [表1].职教高考班,
    [表1].语文,
    [表1].数学,
    [表1].英语,
    [表1].物理,
    [表1].化学,
    [表1].语数外总分,
    DCount("*","表1",
        "[职教高考班] = '" & Replace([表1].[职教高考班],"'","''") & "'" &
        " AND ([语数外总分] > " & [表1].[语数外总分] &
        " OR ([语数外总分] = " & [表1].[语数外总分] & " AND [语文] > " & [表1].[语文] & ")" &
        " OR ([语数外总分] = " & [表1].[语数外总分] & " AND [语文] = " & [表1].[语文] & " AND [数学] > " & [表1].[数学] & ")" &
        " OR ([语数外总分] = " & [表1].[语数外总分] & " AND [语文] = " & [表1].[语文] & " AND [数学] = " & [表1].[数学] & " AND [英语] > " & [表1].[英语] & ")"
    ) + 1 AS 校内名次
INTO [排名结果]
FROM [表1]
WHERE
    -- ① 未被普高录取
    NOT EXISTS (
        SELECT 1 FROM [表2]
        WHERE [表2].身份证号 = [表1].身份证号
    )
    -- ② 语数外达标
    AND Nz([表1].语数外总分,0) >= 205
    -- ③ 有有效志愿
    AND [表1].职教高考班 Is Not Null
    AND [表1].职教高考班 <> '';


-- -------------------------------------------------------------------------
-- 步骤 4：生成最终 [录取结果] 表 —— 关联表3 计划数，判定录取/候补
--
-- 思路：
--   [排名结果] LEFT JOIN [表3]：用"职教高考班"里的学校代码匹配表3。
--   表3 的学校名称格式是 _3714000001_德州信息工程…，
--   表1 的志愿格式是 <3714000001>德州信息工程…，
--   用 InStr + Mid 提取代码后做字符串匹配。
-- -------------------------------------------------------------------------
SELECT
    [排名结果].序号,
    [排名结果].区县,
    [排名结果].就读学校,
    [排名结果].报名号,
    [排名结果].姓名,
    [排名结果].性别,
    [排名结果].身份证号,
    [排名结果].民族,
    [排名结果].职教高考班 AS 志愿学校,
    [排名结果].语文,
    [排名结果].数学,
    [排名结果].英语,
    [排名结果].物理,
    [排名结果].化学,
    [排名结果].语数外总分,
    [排名结果].校内名次,
    [表3].计划数 AS 招生计划,
    IIf(
        [排名结果].校内名次 <= Nz([表3].计划数,0),
        '录取',
        '候补'
    ) AS 录取状态
INTO [录取结果]
FROM [排名结果]
LEFT JOIN [表3]
ON Mid([排名结果].职教高考班,
       InStr([排名结果].职教高考班,'<')+1,
       InStr([排名结果].职教高考班,'>')-InStr([排名结果].职教高考班,'<')-1)
   = Mid([表3].[学校名称], 2, InStr(2,[表3].[学校名称],'_')-2);


-- ==========================================================================
-- 验证查询（可选，运行后查看核对数字）
-- ==========================================================================

-- ① 录取状态汇总
SELECT 录取状态, Count(*) AS 人数
FROM [录取结果]
GROUP BY 录取状态;
-- 预期：录取 104，候补 680


-- ② 各校录取情况
SELECT
    志愿学校,
    招生计划,
    Sum(IIf(录取状态='录取',1,0)) AS 已录取,
    Count(*) AS 达标候选总数
FROM [录取结果]
GROUP BY 志愿学校, 招生计划
ORDER BY 志愿学校;
-- 预期：17 校，每校 已录取 = 招生计划


-- ③ 导出某校录取名单（以"云天"为例，复制到新查询运行后右键导出 Excel）
SELECT *
FROM [录取结果]
WHERE 录取状态 = '录取'
  AND 志愿学校 Like '*云天*'
ORDER BY 校内名次;


